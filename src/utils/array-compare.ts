export function arrayCompare(firstArray: number[], secondArray: number[]): boolean;
export function arrayCompare(firstArray: string[], secondArray: string[]): boolean;
export function arrayCompare<T>(firstArray: T[], secondArray: T[]): boolean {

    if (firstArray.length !== secondArray.length) {
        return false;
    }

    for (let i = 0; i < firstArray.length; i++) {
        if (firstArray[i] !== secondArray[i]) {
            return false;
        }
    }
    return true;
}
